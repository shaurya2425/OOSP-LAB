# Initialize the list
friends = ["Alice", "Bob", "Charlie", "David", "Eva", "Frank", "Grace", "Helen", "Ivy", "Jack"]

#a. Print the first name.
print("First name: ",friends[0])

#b. Print the last name.
print("Last name: ",friends[-1])

# c. Print names from third to fifth index (i.e., index 3 to 5)
print("Names from index 3 to 5:", friends[3:6])

# d. Print names in reverse order
print("Names in reverse order:", friends[::-1])

#e. Print names from 8th to 1st in reverse order.
print("Names from 8th to 1st in reverse order: ", friends[8:0:-1])